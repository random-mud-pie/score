static const string unknownStr = "UNKNOWN";

Tag tagOfGumboTag(GumboTag gumboTag) {
  auto found = gumboToTagMap.find(gumboTag);
  if (found != gumboToTagMap.end()) {
    return found->second;
  }
  return Tag::UNKNOWN;
}

GumboTag gumboTagOfTag(Tag tag) {
  auto found = tagToGumboMap.find(tag);
  if (found != tagToGumboMap.end()) {
    return found->second;
  }
  return GUMBO_TAG_UNKNOWN;
}

const std::string& stringOfTag(Tag tag) {
  auto found = tagToStringMap.find(tag);
  if (found != tagToStringMap.end()) {
    return found->second;
  }
  return unknownStr;
}

